void CEXP2View::OnDraw(CDC* pDC)
{
	CEXP2Doc* pDoc = GetDocument();
	ASSERT_VALID(pDoc);
	if (!pDoc)
		return;

	// TODO: 在此处为本机数据添加绘制代码
	CString s2 = pDoc->s;
	CString s3=pDoc->s1;
	pDC->TextOutW(100, 100, s3);
	pDC->TextOutW(100, 200, s2);
}


